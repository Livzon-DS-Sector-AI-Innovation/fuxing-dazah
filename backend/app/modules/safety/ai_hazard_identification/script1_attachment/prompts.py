# Prompt 构建
# ═══════════════════════════════════════════════════════════════

def build_prompt(context_text: str, knowledge_context: str | None = None) -> str:
    """构建完整的 4 段式 user prompt。

    Args:
        context_text: INPUT_INFO 段 — 从 Input 模型构建的上下文字符串
        knowledge_context: REFERENCE_DOCS 段 — 知识库注入内容
    """
    sections: list[str] = []

    # ── 段1: INPUT_INFO ──
    sections.append("## 输入信息\n\n" + (context_text or "（无输入信息）"))

    # ── 段2: WORK_RULES ──
    sections.append(WORK_RULES)

    # ── 段3: REFERENCE_DOCS ──
    if knowledge_context:
        sections.append(
            "## 参考文档（知识库）\n\n"
            "以下为本企业相关的知识库信息，优先参照：\n\n"
            + knowledge_context
        )

    # ── 段4: OUTPUT_FORMAT ──
    sections.append(OUTPUT_FORMAT)

    return "\n\n".join(sections)


