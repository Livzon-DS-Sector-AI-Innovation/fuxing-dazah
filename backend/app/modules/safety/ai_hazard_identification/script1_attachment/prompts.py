WORK_RULES = """## 工作规则

⚠️ **首要原则**：上方的「知识库」提供了企业产品、建筑物、危险有害因素等参考信息。
所有判断必须优先参照知识库内容，不得依赖你的训练记忆。

### 1. 范围限定
- 仅提取与当前岗位、当前生产步骤**直接相关**的信息
- 不做跨岗位泛化，不提取上下游工序的信息（除非与当前步骤有直接物料/能量交换）
- 若同一岗位涉及多个工序，仅提取当前 production_step 对应的内容

### 2. 信息来源
- 主要来源：附件文档内容（attachment_text）
- 辅助参考：知识库中的产品一览表、建（构）筑物一览表、安全管理现状
- **仅提取文档中明确出现的信息**，不得编造、推断或补全

### 3. 三个输出字段的提取标准

#### specific_activity（具体作业活动）
- 描述「做什么 + 怎么做」
- 格式：「操作名称：操作具体过程描述」
- 示例：「加盐酸调pH：佩戴防飞溅面屏和耐酸碱手套，确认罐压为0后打开罐盖，倒入盐酸100L，关闭罐盖，氮气置换三次」
- 必须包含：操作目的 + 具体步骤 + 关键参数（温度、压力、时间、用量等）

#### equipment_facilities（设备设施）
- 列出该步骤涉及的所有主要设备、管道、仪表
- 格式：「设备名称（类型/材质，规格参数）」多个以顿号或逗号分隔
- 示例：「反应罐R201（搪玻璃，5000L）、pH计（在线监测）、氮气置换管线、压力表」
- 必须包含设备位号/编号（如文档中有）

#### raw_auxiliary_materials（原辅料）
- 列出涉及的所有物料，含危化品、蒸汽、氮气、工艺用水等
- 格式：「物料名（浓度/规格，用量）」多个以顿号或逗号分隔
- 示例：「盐酸（30%，100L）、氮气、纯化水」
- 必须注明危化品的浓度和用量（如文档中有）

### 4. 约束
- 不擅自修改、推断或美化原文内容
- 不做危险源分析（那是脚本2的职责）
- 不做风险评价（那是脚本3/5/7的职责）
- 每条信息尽量附依据来源（如「见附件第3页第2段」）

### 5. 信息不足处理
- 若附件未上传或无法解析（attachment_text 为空或为 None），三个字段均填「待人工确认」
- 若某个字段信息不足但其他字段可提取，单独对该字段填「待人工确认」并注明原因
- **严禁因部分字段不足而全部放弃填写**"""


